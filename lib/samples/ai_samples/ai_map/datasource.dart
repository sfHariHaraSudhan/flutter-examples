class MapLocation {
  MapLocation({
    required this.name,
    required this.details,
    required this.coordinates,
    required this.address,
    required this.image,
  });

  factory MapLocation.fromJson(Map<String, dynamic> json) {
    return MapLocation(
      name: json['Name'],
      details: json['Details'],
      coordinates: MapLatLng(json['Latitude'], json['Longitude']),
      address: json['Address'],
      image: '',
    );
  }
  final String name;
  final String details;
  final MapLatLng coordinates;
  final String address;
  final String image;
}
